package Conditional; // Declares the package name "Conditional"

public class intro { // Defines a class named "intro"

    public static void main(String args[]) { // Main method, the entry point of the program

        int n = -5; // Declares and initializes an integer variable "n" with the value -5

        if (n >= 0) { // If the value of "n" is greater than or equal to 0, execute the following
                      // block of code
            System.out.println("Positive"); // Print "Positive" to the console
        } else { // If the value of "n" is less than 0, execute the following block of code
            System.out.println("Negative"); // Print "Negative" to the console
        }
    }
}
